/**
 * [getRegisterVerifyInDay Obtiene la cantidad de veces que sale el dni y el telefono en el dia]
 * @param  [type] $dni         [dni del cliente]
 * @return [type]              [description]
 */
  public static function getRegisterVerifyInDay($phone, $dni)
  {
    $cantidad = self::getConnect('R')
      ->where([
        ['dni_client', $dni],
        ['phone_verify', $phone],
        ['type_verify', 'VERIFIED']])
      ->whereBetween('date_reg', [date('Y-m-d 00:00:00'), date('Y-m-d 23:59:59')])
      ->get();

    return (!empty($cantidad)) ? count($cantidad) : 0;
  }

/**
 * [getRegisterVerifyByPhone Busca el ultimo registro que se tiene del telefono con el dni del cliente]
 * @param  [type] $phone      [Telefono que se piensa verificar]
 * @param  [type] $clientDni  [DNI de la persona que tiene segun el telefono a verificar]
 * @param  [type] $sellerMail [Persona que esta haciendo la peticion de verificacion]
 * @return [type]             [description]
 */
  public static function getCodeVerifyByPhone($phone, $clientDni)
  {
    $code = "";
    $verify_id = "";
    //Se revisa si el telefono lo tiene tiene registrado otro cliente como telefono primario
    $cliente = Client::getProspectByPhone($phone, $clientDni);
    if (!empty($cliente)) {
      //Significa que otro cliente tiene el telefono registrado
      $code = "IS_PHONE_CLIENT";
    }

    if (empty($code)) {
      //Se revisa si el telefono esta registrado en algun usuario de netwey
      $PhoneNetwey = User::existPhoneUser($phone);
      if (!empty($PhoneNetwey)) {
        //Significa que el telefono lo tiene un vendedor de netwey
        $code = "IS_PHONE_USER";

        $UserNetwey = User::existPhoneUser($phone, session('user'));
        if (empty($UserNetwey)) {
          //Significa que el telefono lo tiene el vendedor de netwey que esta registrando la verificacion
          $code = "IS_PHONE_USER_SELLER";
        }
      }

      if (empty($code)) {
        $register = self::getConnect('R')
          ->where([
            ['phone_verify', $phone],
            ['status', '!=', 'T']])
          ->orderBy('id', 'DESC')
          ->first();

        $code = "PHONE_EMPTY";
        //PHONE_OK Significa que no esta registrado en ningun lugar de netwey
        if (!empty($register)) {
          //significa que esta en algun proceso de verificacion de telefono
          //              //enum('CREATE','VERIFIED','REJECTED','AUTHORIZED','T')

          $code = $register->status;
          $verify_id = $register->id;
        }
      }
    }
    return array('code' => $code, 'verify_id' => $verify_id);
  }

  public static function getMsgVerify($code)
  {
    $msg = null;
    switch ($code) {
      case "IS_PHONE_CLIENT":
        $msg = "El telefono ingresado lo tiene otro cliente de Netwey";
        break;

      case "IS_PHONE_USER":
        $msg = "El telefono ingresado lo tiene un usuario de Netwey";
        break;

      case "IS_PHONE_USER_SELLER":
        $msg = "El telefono ingresado lo tiene un usuario de Netwey que realiza la venta";
        break;

      case "PHONE_EMPTY":
        $msg = "El telefono ingresado no posee registros en otro cliente de Netwey";
        break;

      case "CREATE":
        $msg = "El telefono ingresado posee registros de creado en espera de acciones segun el tipo de registro (Autorizacion de mesa o verificacion SMS)";
