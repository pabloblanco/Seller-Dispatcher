  public static function verifiedAutorized($phone, $clientDni)
  {
    $response = ['success' => false, 'title' => "No se pudo verificar el status de solicitud de autorizacion sin verificar el celular", 'msg' => "Fallo el registro de datos", 'icon' => "error", 'code' => 'ERR_DAT'];

    $consult = self::getConnect('R')
      ->where([
        ['phone_verify', $phone],
        ['dni_client', $clientDni],
        ['type_verify', 'AUTHORIZED'],
        ['status', '!=', 'T']])
      ->orderBy('id', 'DESC')
      ->first();

    $msg = "No se encuentra registro de solicitud de autorización";
    $icon = "";
    $code = "EMPTY";

    if (!empty($consult)) {
      $response['success'] = true;
      $msg = "Aun en espera por revisión por parte de mesa de control";
      $icon = self::getIconAutorized("LOADING");

      switch ($consult->status) {
        case 'CREATE':
          $code = "CREATE";
          $msg = "Aun en espera por revisión por parte de mesa de control. Solicitud: #" . $consult->id;
          break;

        case 'REJECTED':
          $code = "REJECTED";
          $msg = "La solicitud de autorización para el telefono fue rechazado por mesa de control";
          $icon = self::getIconAutorized("REJECTED");
          break;

        case 'AUTHORIZED':
          $code = "AUTHORIZED";
          $msg = "La solicitud de autorización fue aprobado por parte de mesa de control";
          $icon = self::getIconAutorized("AUTHORIZED");
          break;

        default:
          $code = $consult->status;
          $msg = "La solicitud de autorización se encuentra en un status no definido para el proceso";
          $icon = "";
          break;
      }
    }
    $response['title'] = "Estatus de solicitud consultada";
    $response['msg'] = $msg;
    $response['icon'] = $icon;
    $response['code'] = $code;
    return $response;
  }

  public static function getLastRegisterVerify($phone, $dni)
  {
    $register = self::getConnect('R')
      ->where('phone_verify', $phone)
      ->whereNotIn('status', ['T', 'CREATE'])
      ->orderBy('id', 'DESC')
      ->first();

    if (!empty($register)) {
      //Evaluamos a que cliente esta asociado el registro
      $client = Client::getClientByDNI($register->dni_client);
      $nameClient = "";
      if (!empty($client)) {
        $nameClient = ". Registro del cliente: " . $client->name . ' ' . $client->last_name;
      }

      $nameSeller = "";
      $UserNetwey = User::getUserByEmail($register->email_seller);
      if (!empty($UserNetwey)) {
        $nameSeller = ". Solicitud cargada por el vendedor: " . $UserNetwey->name . ' ' . $UserNetwey->last_name;
      }

      switch ($register->status) {
        case 'REJECTED':
          $msg = "El telefono fue rechazado por parte de mesa de control";
          break;

        case 'AUTHORIZED':
          $msg = "El telefono tiene registros de aprobacion por parte de mesa de control";
          break;

        case 'VERIFIED':
          $msg = "El telefono posee un registro de verificacion exitosa";
          break;

        default:
          $msg = "El telefono se encuentra en un status no definido para el proceso";
          break;
      }
      $response['success'] = true;
      $response['title'] = "Hay registros del telefono";
      $response['msg'] = $msg . $nameClient . $nameSeller;
      $response['icon'] = "success";
      $response['code'] = 'OK';
    } else {
      $response = ['success' => false, 'title' => "No hay registros del telefono", 'msg' => "No hay historico de verificacion para el telefono consultado", 'icon' => "error", 'code' => 'DAT_EMP'];
    }
    return $response;
  }

