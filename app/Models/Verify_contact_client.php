  public static function registerNewToken($phone, $clientDni, $newToken, $Obs = null)
  {
    $response = ['success' => false, 'title' => "No se pudo registrar el token de verificación para el celular", 'msg' => "Fallo el registro de datos", 'icon' => "error", 'code' => 'ERR_DAT'];

    try {
      //$newToken = Common::getTokenVerify();
      //Token permitido al formato ABC123
      if (preg_match("/^[a-zA-Z]{3}[0-9]{3}$/", $newToken)) {

        $register = self::getConnect('W')
          ->insertGetId([
            'phone_verify' => $phone,
            'dni_client' => $clientDni,
            'token_verify' => $newToken,
            'email_seller' => session('user'),
            'type_verify' => 'VERIFIED',
