      Log::error($txmsg);
      $response['msg'] = $txmsg;
    }
    return $response;
  }

  public static function deleteOldVerified($phone, $clientDni, $type = ["VERIFIED"], $Remove = ['CREATE'])
  {
    try {
      self::getConnect('W')
        ->where([
          ['dni_client', $clientDni],
          ['phone_verify', $phone]])
        ->whereIn('type_verify', $type)
        ->whereIn('status', $Remove)
        ->update([
          'status' => 'T',
          'date_update' => date('Y-m-d H:i:s'),
          'observacion' => "Eliminacion por re-envio o descarte de token"]);

      return array('success' => true, 'title' => "Eliminación de registro previos", 'msg' => "Eliminación satisfactoria", 'icon' => "success", 'code' => 'OK');

    } catch (Exception $e) {
      $txmsg = 'Error al eliminar el registro de verificación del cliente. ' . (String) json_encode($e->getMessage());
      Log::error($txmsg);
      return array('success' => false, 'title' => "Eliminación de registro previos no se pudieron remover", 'msg' => $txmsg . '  (MV229)', 'icon' => "error", 'code' => 'FAIL');
    }
  }

  public static function verifyToken($phone, $clientDni, $token)
  {
    try {
      $verify = self::getConnect('W')
        ->where([
          ['dni_client', $clientDni],
          ['phone_verify', $phone],
          ['type_verify', 'VERIFIED'],
          ['status', 'CREATE'],
          ['token_verify', $token]])
        ->first();

      if (!empty($verify)) {
        //existe el token y se puede confirmar la verificacion
        $verify->status = "VERIFIED";
        $verify->date_update = date('Y-m-d H:i:s');
        $verify->observacion = "Verificado desde Seller";
        $verify->save();
        //Actualizo le telefono de contacto
        $updateC = Client::updatePhoneContact($clientDni, $phone, $verify->id);
        if ($updateC['success']) {
          return array('success' => true, 'title' => "Verificación de celular OK", 'msg' => "El telefono " . $phone . " fue verificado con exito!", 'icon' => "success", 'code' => 'OK');
        }
        return array('success' => false, 'title' => "Verificación de celular no se pudo completar", 'msg' => "No se pudo completar la actualizacion de datos posterior a la verificación del celular " . $phone . " (MV256)", 'icon' => "error", 'code' => 'FAIL_UP');
      } else {
        return array('success' => false, 'title' => "Verificación de celular invalido", 'msg' => "No se encuentra registros del token " . strtoupper($token) . " para el celular " . $phone . " en sistema, verifique los datos e intente nuevamente (MV258)", 'icon' => "error", 'code' => 'FAIL_TOKEN');
      }
    } catch (Exception $e) {
      $txmsg = 'Error al realizar el proceso de revision del token de verificación del cliente ' . $clientDni . ' ' . (String) json_encode($e->getMessage());
      Log::error($txmsg);
      return array('success' => false, 'title' => "Verificacion de celular fallo", 'msg' => $txmsg, 'icon' => "error", 'code' => 'FAIL_BD');
    }
  }

